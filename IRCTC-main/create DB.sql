CREATE PROCEDURE `checkwait`(
IN tid int, IN tim datetime,IN pnrid varchar(26)
)
BEGIN 
    DECLARE done INT DEFAULT 0;
    DECLARE st1 varchar(10);
    DECLARE st2 VARCHAR(10);
    declare cla varchar(3);
    declare pn varchar(26);
    declare pt varchar(1000);

    DECLARE cur CURSOR FOR
        SELECT pnr,source,destination,path,class
        FROM Ticket as t where t.pnr like concat(pnrid,'%') and t.cnf = 0 order by t.booked_at;

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    OPEN cur;
    read_loop: LOOP
        FETCH cur INTO pn,st1,st2,pt,cla;
        IF done = 1 THEN
            LEAVE read_loop;
        END IF;
        if(REUSE_SEATS(tid,tim,st1,st2,cla)>0)then 
        select r.path , r.S_N , r.C_N
        INTO @P, @S, @C
        from reuse_seat as r
        where r.train_id=tid and r.start_time = tim and r.class = cla and r.path like concat('%',pt,'%')
        ORDER BY LENGTH(path)
		LIMIT 1
        FOR UPDATE;
        DELETE FROM reuse_seat as r
		WHERE r.train_id = tid AND r.start_time = tim AND r.class = cla AND r.path = @P AND r.C_N=@C AND r.S_N=@S;
		set @l = length(@P);
        set @p1 = LOCATE(@s1, @P);
        set @p2 = LOCATE(@s2, @P);
        set @str0 = SUBSTRING(@P, @p1, @l-@p1+1);
        set @str3 = SUBSTRING(@P, @p2, @l-@p2+1);
        set @l1 = LOCATE('|',@str0)-1;
        set @l2 = LOCATE('|',@str3)-1;
		if @l2<0 then 
		set @l2 = length(@str3)+1;
		end if;
        set @str1 = SUBSTRING(@P, 1, @p1+@l1-1);
        set @str2 = SUBSTRING(@str0, 1, @p2-@p1+@l2);
		update Ticket
        set cnf=1,C_N=@C,S_N=@S where pnr = pn;
        if (LOCATE('|', @str1)>0) then
			insert into reuse_seat (`train_id`, `start_time`, `class`, `C_N`, `S_N`, `path`) values (train_id, departure_date_time, class,C,S,@str1);
        end if;
        if (LOCATE('|', @str3)>0) then
			insert into reuse_seat (`train_id`, `start_time`, `class`, `C_N`, `S_N`, `path`) values (train_id, departure_date_time, class,C,S,@str3);
        end if;
        end if;
    END LOOP;
    CLOSE cur;
END$$
