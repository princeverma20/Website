CREATE FUNCTION FIRST_SEATS(id int, t datetime, station_code1 VARCHAR(10), station_code2 VARCHAR(10),class varchar(3))
RETURNS INT
DETERMINISTIC
READS SQL DATA
BEGIN
	DECLARE result INT;
    DECLARE C INT;
    DECLARE S INT;
    
    SELECT
        CASE class
            WHEN 'AC1' THEN f.AC1
            WHEN 'AC2' THEN f.AC2
            WHEN 'AC3' THEN f.AC3
           WHEN 'SLP' THEN f.SLP
           WHEN 'GEN' THEN f.GEN
        END,
        CASE class
            WHEN 'AC1' THEN f.SAC1
            WHEN 'AC2' THEN f.SAC2
            WHEN 'AC3' THEN f.SAC3
           WHEN 'SLP' THEN f.SSLP
           WHEN 'GEN' THEN f.SGEN
        END INTO C,S
    FROM full_first_seat AS f
    WHERE f.train_id = id AND f.start_time = t;

		CASE class
            WHEN 'AC1' THEN set result = GREATEST(0, C - 1) * 18 + LEAST(1, C) * S;
            WHEN 'AC2' THEN set result = GREATEST(0, C - 1) * 54 + LEAST(1, C) * S;
            WHEN 'AC3' THEN set result = GREATEST(0, C - 1) * 72 + LEAST(1, C) * S;
           WHEN 'SLP' THEN set result = GREATEST(0, C - 1) * 80 + LEAST(1, C) * S;
           WHEN 'GEN' THEN set result = GREATEST(0, C - 1) * 90 + LEAST(1, C) * S;
        END CASE;
    RETURN result;
END;
