CREATE FUNCTION Waiting(id int, t datetime, class varchar(3))
RETURNS INT
DETERMINISTIC
READS SQL DATA
BEGIN
	DECLARE result INT;
	SET @tid = id;
	set @pn1 = DATE(t);
	set @pn2 = DATE_FORMAT(TIME(t), '%H:%i');
    set @lola = concat(@tid,'-',@pn1,'-',@pn2,'-%');
    
    select count(*) INTO result from Ticket as r
    where r.pnr  like @lola and r.class = class and r.cnf = false;
    RETURN result;
END;
