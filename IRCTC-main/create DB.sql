CREATE PROCEDURE before_search()
BEGIN
    -- Declare local variables to store the values fetched by the cursor
    DECLARE v_train_id INT;
    DECLARE v_start_time DATETIME;
    DECLARE v_pnr INT;
    DECLARE v_AC1 INT;
    DECLARE v_SAC1 INT;
    DECLARE v_AC2 INT;
    DECLARE v_SAC2 INT;
    DECLARE v_AC3 INT;
    DECLARE v_SAC3 INT;
    DECLARE v_SLP INT;
    DECLARE v_SSLP INT;
    DECLARE v_GEN INT;
    DECLARE v_SGEN INT;
    DECLARE done INT DEFAULT 0;

    -- Cursor to select all past rows
    DECLARE cur CURSOR FOR
    SELECT f.train_id, f.start_time, f.pnr, f.AC1, f.SAC1, f.AC2, f.SAC2, f.AC3, f.SAC3, f.SLP, f.SSLP, f.GEN, f.SGEN
    FROM full_first_seat AS f
    WHERE f.start_time < NOW();

    -- Declare handler for cursor loop termination
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    OPEN cur;

    -- Start the cursor loop
    REPEAT
        FETCH cur INTO 
            v_train_id, v_start_time, v_pnr, v_AC1, v_SAC1, v_AC2, v_SAC2, v_AC3, v_SAC3, v_SLP, v_SSLP, v_GEN, v_SGEN;
        
        IF NOT done THEN
            -- Calculate the new start time
            SET v_start_time = DATE_ADD(v_start_time, INTERVAL 7*18 DAY);
            
            -- Keep adding 7*18 days until the new start time is in the future
            WHILE v_start_time < NOW() DO
                SET v_start_time = DATE_ADD(v_start_time, INTERVAL 7*18 DAY);
            END WHILE;
            
            -- Insert the updated row with the new start time
            INSERT INTO full_first_seat (train_id, start_time, pnr, AC1, SAC1, AC2, SAC2, AC3, SAC3, SLP, SSLP, GEN, SGEN)
            VALUES (v_train_id, v_start_time, v_pnr, v_AC1, v_SAC1, v_AC2, v_SAC2, v_AC3, v_SAC3, v_SLP, v_SSLP, v_GEN, v_SGEN);
        END IF;
    UNTIL done END REPEAT;

    -- Close the cursor
    CLOSE cur;

    -- Delete past rows
    DELETE FROM full_first_seat
    WHERE start_time < NOW();
END$$
