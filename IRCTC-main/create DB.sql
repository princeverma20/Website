CREATE FUNCTION REUSE_SEATS(id int, t datetime, station_code1 VARCHAR(10), station_code2 VARCHAR(10),class varchar(3))
RETURNS INT
DETERMINISTIC
READS SQL DATA
BEGIN
	DECLARE result INT;
	SET @s1 = station_code1;
	SET @s2 = station_code2;
	SET @lola = CONCAT('%', @s1,'%',@s2,'%');
    
    select count(*) INTO result from reuse_seat as r
    where r.train_id = id and r.start_time = t and r.path like @lola and r.class = class;
    RETURN result;
END;
